def verifica_positivo(a):
    if a > 0:
        print(f"O número {a} é positivo")
    else:
        print(f"O número {a} é negativo")
        return a

    
def verifica_maior_zero(b):
    if b > 0:
        print(f"O número {b} é maior que zero")
    else:
        print(f"O número {b} é menor que zero")
        return b

